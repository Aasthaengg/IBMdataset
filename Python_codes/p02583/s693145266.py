N=int(input())
L=list(map(int,input().split()))
ans=0
for i in range(len(L)-2):
    for j in range(i+1,len(L)-1):
        if L[i]!=L[j]:
            for k in range(j+1,len(L)):
                if L[i]!=L[k] and L[j]!=L[k]:
                    M=max([L[i],L[j],L[k]])
                    if M==L[i] and M<L[j]+L[k]:
                        ans+=1
                    elif M==L[j] and M<L[k]+L[i]:
                        ans+=1
                    elif M==L[k] and M<L[i]+L[j]:
                        ans+=1

print(ans)






        



















    

                
            





    









        

    









    

        




    















    



    





    






        






    
















        


























            
    


    


    
    














    


    












