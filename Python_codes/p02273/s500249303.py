import math

X = 0
Y = 1
sin60 = math.sin(math.radians(60))
cos60 = math.cos(math.radians(60))

def fn(P1, P2, n, l):
    l += 1
    if l > n:
        return [P1,P2]
    
    x3 = (P2[X] -P1[X]) / 3
    y3 = (P2[Y] -P1[Y]) / 3
    xa = x3 + P1[X]
    ya = y3 + P1[Y]
    xc = P2[X] - x3
    yc = P2[Y] - y3
    xb = (xc - xa) * cos60 - (yc - ya) * sin60 + xa
    yb = (xc - xa) * sin60 + (yc - ya) * cos60 + ya
    Pa = [xa,ya]
    Pb = [xb,yb]
    Pc = [xc,yc]
    Ps = [P1]
    f1 = fn(P1,Pa,n,l)
    f2 = fn(Pa,Pb,n,l)
    f3 = fn(Pb,Pc,n,l)
    f4 = fn(Pc,P2,n,l)
    Ps.extend(f1[1:])
    Ps.extend(f2[1:])
    Ps.extend(f3[1:])
    Ps.extend(f4[1:])
    return Ps

