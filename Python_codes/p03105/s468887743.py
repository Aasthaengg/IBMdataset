'''
問題：
    高橋くんは、自動販売機でジュースを買ったときの音が好きです。
    その音は 1回 A円で聞くことができます。

    高橋くんは B円持っていますが、
    お気に入りの音を C回聞くと満足するため、
    B円で最大 C回まで聞けるだけ聞きます。
    高橋くんはお気に入りの音を何回聞くことになるでしょうか。
'''

'''
制約：
    入力は全て整数である。
    1 ≤ A, B, C ≤ 100
'''

# 標準入力から A, B, C の値を取得する
a, b, c = map(int, input().split())

# お気に入りの音を聞ける回数を計算して出力する
result = 0
if a * c < b:
    result = c
else:
    result = int(b / a)

print(result)
