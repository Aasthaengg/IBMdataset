S = input()

if S == ('SUN'):
    print(7)
elif S == ('MON'):
    print(6)
elif S == ('TUE'):
    print(5)
elif S == ('WED'):
    print(4)
elif S == ('THU'):
    print(3)
elif S == ('FRI'):
    print(2)
elif S == ('SAT'):
    print(1)
else:
    print('入力に誤りがあります。SUN,MON,TUE,WED,THU,FRI,SATのいずれかを入力してください。')
