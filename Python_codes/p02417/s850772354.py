alphabet_list = [chr(i) for i in range(97, 97+26)]
alphabet_dict = {}
for a in alphabet_list:
    alphabet_dict.update({a:0})

for strings in sys.stdin:
    for s in strings:
        if (s.isalpha()):
            s = s.lower()
            alphabet_dict[s] += 1

for key in sorted(alphabet_dict.keys()):
    print(str(key) + ' : '+ str(alphabet_dict[key]))