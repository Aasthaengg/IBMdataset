while True:
    H, W = list(map(int, input().split()))
    if H == 0 & W == 0:
        break

    #????????¶???#??¨.????????????????????????#??????????§?
    for i in range(1,H+1):
        line = []
        for j in range(1,W+1):
            if ((i+j) % 2) == 0:
                line.append('#')
            else:
                line.append('.')
        print(''.join(line))

    print("")