# 高橋君モンスター体力A/攻撃力B
# 青木君ﾓﾝｽﾀｰ体力C/攻撃力D
# 高橋→青木→高橋→青木の順番
# 相手のﾓﾝｽﾀｰの体力を自分のﾓﾝｽﾀｰの攻撃分減らせる。C＝C-B/A=A-D
# どちらかの体力が０以下になるまで戦い続ける。 A<0 B<0
# 先に自分のﾓﾝｽﾀｰの体力が０になった方が負け。
# 高橋君が勝つなら”Yes”負けるなら”No”を出力

A,B,C,D = map(int,input().split())

while A>0 and C>0:
    C = C-B
    A = A-D

if C <= 0:
    print("Yes")
else:
    print("No")

