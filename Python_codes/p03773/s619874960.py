# A - Remaining Time

# 現在の時刻は、24時間表記（0:00 ~ 23:59）で A時 ちょうど
# コンテストはちょうど B 時間後に始まる

# コンテストの開始時刻は、24時間表記で何時ちょうどですか？

# A B を標準入力から得る
A, B = map(int, input().split())
# print(A, B)

# A + B が24を超えた場合、 (A + B) - 24 を行う必要がある。

# (A + B) % 24 ⇔ (A + B) を 24 で 割った余りを表示
# こうすることで、24以上の時も同様に表示できる

answer = (A + B) % 24

# 結果を出力
print(answer)
