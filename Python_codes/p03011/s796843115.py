'''
問題：
    空港 A, B, C があり、それぞれの空港の間では、双方向に飛行機が運航しています。

    空港 A, B 間の飛行時間は片道 P時間、
    空港 B, C 間の飛行時間は片道 Q時間、
    空港 C, A 間の飛行時間は片道 R時間です。

    いずれかの空港からスタートして他の空港に飛行機で移動し、
    さらにそのどちらでもない空港に飛行機で移動するような経路を考えます。
    飛行時間の和は最短で何時間になるでしょうか。
'''

'''
制約：
    1 ≤ P, Q, R ≤ 100
    入力は全て整数である
'''

# 標準入力から P, Q, R の値を取得する
p, q, r = map(int, input().split())

# A-B-C の経路を計算して、最小値を出力する
result = [p + q, q + r, r + p]  # 全ての経路をリストに格納する
print(min(result))  # リストの最小値を出力する
