# 入力値を受け取る
s = input()

# 入力値の3文字目を受け取る
a = s[2]

# 入力値の4文字目を受け取る
b = s[3]

# 入力値の3文字目と4文字目が同じでないなら'No'を表示する
if a != b:
    print('No')
    exit()
    
# 入力値の5文字目を受け取る
c = s[4]

# 入力値の6文字目を受け取る
d = s[5]

# 入力値の5文字目と6文字目が同じでないなら'No'を表示する
if c != d:
    print('No')
    exit()

# 上記以外なら'Yes'を表示する
print("Yes")