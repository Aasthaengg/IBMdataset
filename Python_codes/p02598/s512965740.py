def solve():
    N, K = MI()
    A = LI()
    A.sort()
    # print(A)

    def is_ok(arg):
        cnt = 0
        for a in A:
            cnt += ceil(a / arg) - 1

        if cnt > K: return False
        else: return True

    def meguru_bisect(ng, ok):
        while (abs(ok - ng) > 1):
            mid = (ok + ng) // 2
            if is_ok(mid):
                ok = mid
            else:
                ng = mid
        return ok

    l = 0
    r = max(A)+1
    print(meguru_bisect(l, r))

