def main():
    X, Y = list(map(int, input().split()))
    # (1)単純に1ずつ増やすだけ：(5, 10), (-5, -2), (5, 5), (-5, -5), (-5, 0), (0, 5), (0, 0)
    if X * Y >= 0 and X <= Y:
        print(Y - X)
    # (2)反転して1ずつ増やして最後に反転：(-5, -10), (5, 2)
    elif X * Y > 0 and X > Y:
        print(X - Y + 2)
    # (3)1ずつ増やして最後に反転：(-5, 2), (5, -10), (0, -5)
    # (4)反転した後は1ずつ増やすだけ：(5, -2), (-5, 10), (5, 0)
    # (5)反転するだけ：(5, -5), (-5, 5)
    else:
        print(abs(X + Y) + 1)


if __name__ == '__main__':
    main()