'''
問題：
    直角三角形 ABC があります。∠ABC=90° です。

    三角形 ABC の三辺の長さである
    |AB|,|BC|,|CA| が与えられるので、
    直角三角形 ABC の面積を求めて下さい。
    ただし、三角形 ABC の面積は整数となることが保証されています。
'''

'''
制約：
    1 ≦ |AB|,|BC|,|CA| ≦ 100
    入力はすべて整数である。
    三角形 ABC の面積は整数である。
'''

# 標準入力から AB, BC, CA の値を取得する
ab, bc, ca = map(int, input().split())

print(int( ab * bc / 2) )
