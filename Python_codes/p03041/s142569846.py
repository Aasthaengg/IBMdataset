n,k=map(int,input().split())
s=input()
l=[s[i] for i in range(n)]
if l[k-1]=="A":
    l[k-1]="a"
elif l[k-1]=="B":
    l[k-1]="b"
else:
    l[k-1]="c"
    
print("".join(l))
    
    
        
    
    
    
        
        
    
    
    
    
        

        
   
    
    
    
        
        
    
    
    
    
        

      
    
    
        
        
    
    
    
    
        
