"""解説から
1. n以下の整数からxで割り切れる数の数を求めるにはn//xすると良い f(n)
2. b以下の整数からxで割り切れる数の数を求める f(b)
3. a"未満"の整数は除外する必要があるのでf(a-1)を求める
4. f(b)-f(a-1)がa以上b以下の整数からxで割り切れる数の個数となる
5. a==0の時、f(a-1) == -1 となる。f(b) - -1 == f(b) + 1となるので、
    0以上b以下の数のxを求めるときにもきちんと求めることができる
"""
a, b, x = map(int, input().split())
b //= x
a = (a - 1) // x
print(b - a)
