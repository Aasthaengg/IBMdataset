# すぬけくんはレストランに通うのが好きです。
# すぬけくん行きつけのレストランは何を食べても 1食800円で、
# 15食食べる毎にその場で200円もらえます。
# すぬけくんは今までで合計 N食食べました。
# 今までに払った金額を x円、レストランからもらった金額を y円として、
# x − y を求めなさい。

# 標準入力から食事回数（N）を取得する
input_num = int(input())

# x - y （円）を計算し出力する。
x = input_num * 800 # x：今までに払った金額
y = int(input_num / 15) * 200  # y：レストランからもらった金額
result = x - y  # 差額

print(result)
