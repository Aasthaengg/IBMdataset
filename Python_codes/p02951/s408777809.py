'''
問題：
    水を入れる容器が 2つあります。
    容器 1には水を A ミリリットルまで入れることができ、
    水が B ミリリットル入っています。
    容器 2 には水が C ミリリットル入っています。
    容器 2 から容器 1 に入るだけ水を移します。
    容器 2 の中には何ミリリットルの水が残るでしょうか。
'''

'''
制約：
    入力は全て整数である。
    1 ≤ B ≤ A ≤ 20
    1 ≤ C ≤ 20
'''

# 標準入力から A, B, C の値を取得する
a, b, c = map(int, input().split())

# 容器2の水の量を計算して出力する
result = c - (a - b)

if result > 0:
    print(result)
else:
    print(0)    # 水は残ってないので、0 を出力する
