N = int(input())
# (a * b * c) or (d * d * d * e)
# (a, b, c) = (3, 5, 7) -> 105
# (a, b, c) = (3, 5, 11) -> 165
# (a, b, c) = (3, 5, 13) -> 195
# (a, b, c) = (3, 5, 17) -> 255 out
# (a, b, c) = (3, 7, 11) -> 231 out
# (d, e) = (3, 5) -> 135
# (d, e) = (3, 7) -> 189
# (d, e) = (3, 11) -> 297 out
# (d, e) = (5, 3) -> 375 out
# therefore: 105, 135, 165, 189, 195

ans = [0, 105, 135, 165, 189, 195, 1000000000]
for i in range(6):
  if ans[i] <= N < ans[i + 1]:
    print(i)