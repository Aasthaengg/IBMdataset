#ここで求めるのはジャッジ終了時間の期待値である。
#よって1回の実行時間T=max(0,N-M)*100+1900*M、成功率p=1/2^Mとして、
#求める値yは
#y=1*T*p+2*T*p(1-p)+3*T*p(1-p)(1-p)+…
#=(lim N→∞)Σ(1<=i<=N)i*T*p*(1-p)^(i-1)
#=Tp*(lim N→∞)Σ(1<=i<=N)i*(1-p)^(i-1)
#ここで摂動法を用いればΣ(1<=i<=N)i*(1-p)^(i-1)=1/p^2-((1-p)^(N+1))/p^2-((N+1)(1-p)^N)/p
#よってここで1-p<1,p<1より(lim N→∞)Σ(1<=i<=N)i*(1-p)^(i-1)=1/p^2
#よってy=Tp/p^2=T/p=T*2^M

import math
N,M=map(int,input().split())
time=max(0,N-M)*100+1900*M
print(time*pow(2,M))