def calc_triangle(a, b, C):
    from math import sqrt, sin, cos, tan, radians
    c = sqrt(a**2 + b**2 - (2*a*b*cos(radians(C))))
    L = a + b + c
    S = 0.5 * a * b * sin(radians(C))
    h = b * sin(radians(C))
    return S, L, h


