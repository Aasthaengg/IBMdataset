inp = input().split(" ")
n = int(inp[0])
m = int(inp[1])

# n*m ??????????????????
ArrA = []
for i in range(n):
    l = list(map(int,input().split(" ")))
    ArrA.append(l)

# m*1 ??????????????????
ArrB = []
for i in range(m):
    ArrB.append(int(input()))

# ArrA * ArrB
for i in range(n):
    x = 0
    for j in range(m):
        x += ArrA[i][j] * ArrB[j]
    print(str(x))